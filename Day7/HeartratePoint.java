

public class HeartratePoint {
  
    private int time;
    private int heart_rate;
    
    public HeartratePoint(int _time, int _heart_rate) {
      time = _time;
      heart_rate = _heart_rate;
    }
    
    public int getTime() {
      return time;
    }
    
    public int getHeartRate() {
      return heart_rate;
    }
  
  
  
}







