
import java.util.Random;

public class SoccerPlayer {
  
  private String name;
  private double speed;
  
  public SoccerPlayer(String new_name, double new_speed) {
    name = new_name;
    speed = new_speed;
  }
  
  public String toString() {
    return "SoccerPlayer: " + name + " " + speed;
  }
  
  
}









